/**
 * @file pin_config.h
 * @brief RF遥控器硬件引脚配置
 *
 * 基于原理图 SCH_Schematic1_2026-01-16
 * MCU: Seeed XIAO ESP32-C3
 */

#ifndef PIN_CONFIG_H
#define PIN_CONFIG_H

// ============================================================================
// OLED显示屏 (SSD1306 128x64)
// ============================================================================
#define OLED_SCL_PIN        6           // IO6 - I2C时钟线
#define OLED_SDA_PIN        8           // IO8 - I2C数据线
#define OLED_I2C_ADDR       0x3C        // SSD1306 I2C地址

// ============================================================================
// 按键配置 (Active Low - 按下接地)
// ============================================================================
#define BTN_UP_PIN          5           // IO5 - 上键 (SW2)
#define BTN_OK_PIN          9           // IO9 - 确认键 (SW1)
#define BTN_DOWN_PIN        7           // IO7 - 下键 (SW3)

// ============================================================================
// 433MHz RF模块
// ============================================================================
#define RF_433_TX_PIN       0           // IO0 - 433MHz发射 (U1: -T3-433)
#define RF_433_RX_PIN       1           // IO1 - 433MHz接收 (RF1: -R6L_433)

// ============================================================================
// 315MHz RF模块
// ============================================================================
#define RF_315_TX_PIN       2           // IO2 - 315MHz发射 (U2: -T3_315)
#define RF_315_RX_PIN       3           // IO3 - 315MHz接收 (RF2: -R6L_315)

// ============================================================================
// 电池监测 (分压电路)
// ============================================================================
#define BAT_ADC_PIN         4           // IO4 - 电池电压ADC输入

// 分压电阻值 (根据原理图)
#define BAT_DIVIDER_R1      100000      // R25: 100kΩ (上臂电阻)
#define BAT_DIVIDER_R2      10000       // R24: 10kΩ (下臂电阻)

// ADC参数
#define BAT_ADC_RESOLUTION  4095.0      // 12位ADC分辨率
#define BAT_ADC_VREF        3.0         // ADC参考电压

// ============================================================================
// 串口调试
// ============================================================================
#define DEBUG_TX_PIN        21          // TXD (U8 Pin12)
#define DEBUG_RX_PIN        20          // RXD (U8 Pin11)
#define DEBUG_BAUD_RATE     115200

// ============================================================================
// 电源管理
// ============================================================================
// MT3608升压模块输出电压计算: VOUT = 0.6V * (1 + R1/R2)
// R25=10k, R18=30k => VOUT = 0.6 * (1 + 30/2) = 9.6V
#define BOOST_OUTPUT_VOLTAGE    9.6     // 升压输出电压 (用于RF模块)

// AMS1117-3.3 稳压输出
#define LDO_OUTPUT_VOLTAGE      3.3     // MCU供电电压

#endif // PIN_CONFIG_H
